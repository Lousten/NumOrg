                                    <h3>Corrosion In The Gas And Oil Industry</h3>
                                </div>
                                <div class="text">
                                    <p><b>The Popular Saying “rust Never Sleeps” Is So True In The Oil And Gas Industry.
                                            Corrosion Starts From The Day Of Manufacture, And Steel Surfaces Whether
                                            Easily Accessible Or Not Must Be Protected, Inspected And Maintained.</b>
                                    </p>
                                    <p>Oil and gas infrastructure and equipment is mainly made of steel. Extraction is
                                        increasingly from the seabed, exposing platforms to a harsh maritime
                                        environment. Processing, distribution and storage infrastructure, also
                                        principally made of steel, typically operate under harsh environments, and their
                                        internal surfaces are exposed to corrosive by-products of oil and gas. All this
                                        makes corrosion a major issue in the industry.</p>
                                    <p>The popular saying “rust never sleeps” is so true in the oil and gas industry.
                                        Corrosion starts from the day of manufacture, and steel surfaces whether easily
                                        accessible or not must be protected, inspected and maintained.</p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h3>The Changing World Of Oil And Gas Extraction</h3>
                                </div>
                                <div class="text">
                                    <p>Before the late 1960s, most oil and gas wells were located on land (mainly in
                                        deserts) or in shallow estuaries, bays and swamps.</p>
                                    <p>From the 1970s, North Sea oil and gas came into production at seabed depths of 50
                                        to 100 m, and in an often very stormy marine environment.</p>
                                    <p>From the 1990s, deep-offshore (300 m deep or more), and subsequently
                                        ultra-deep-offshore (3000 m deep or more) oil/gas exploitation came on-line.
                                        Shell’s Perdido platform in the Gulf of Mexico is such an ultra-deep well.</p>
                                    <p>Today, almost all of the “newer” exploitable oil/gas is deep under the sea, and
                                        often thousands of metres under the seabed. The great undersea depths, together
                                        with the great depths under the seabed, combined with the salty sea atmosphere,
                                        provide a formidable challenge to corrosion protection – i.e., access,
                                        inspection and renovation of the protective coating.</p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h3>Oil Refineries – Complex Plant Attacked By Acid Corrosion From Inside</h3>
                                </div>
                                <div class="text">
                                    <p>The inner surfaces of the complex refining apparatus are attacked by the acidic
                                        content of the oil being refined. Corrosion is aggravated by the high pressures,
                                        temperatures and flows used in the process.</p>
                                    <p>Corrosion predominantly occurs in the crude distillation unit (CDU) and in the
                                        high vacuum unit (HVU) where the crude oil is distilled in two steps, at
                                        atmospheric pressure and under vacuum, into its major fractions.</p>
                                    <p>The inner surfaces of the steel apparatus react with the oil’s sulphur content,
                                        mainly H2S (“sour” corrosion – which also affects pipelines, as explained
                                        further on). These inner surfaces also react with the oil’s thiol content
                                        (mercaptans) and its naphthenic acids (of generic formula CnH2n-z O2), which are
                                        highly corrosive.</p>
                                    <p>High temperature accelerates the corrosive chemical reactions, and high pressure
                                        and flow intensify the shear stresses on internal pipe surfaces; this degrades
                                        the FeS protective layer, thus facilitating corrosion of the bulk metal.</p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h3>Oil & Gas Pipelines – Attacked From Outside, Eaten-up From Inside</h3>
                                </div>
                                <div class="text">
                                    <img src="assets/images/other/8.jpg" alt="" class="img-flow">
                                    <p>Oil and gas pipelines, whether from the well to the processing facilities, or in
                                        distribution network to end-point users, are deployed over- or under- ground, as
                                        well as underwater, over vast distances and in varied terrain.</p>
                                    <p>Corrosion caused by the transported substance, whether oil or gas, can be divided
                                        into “sweet” and “sour” corrosion: Sweet corrosion takes place in systems
                                        containing only carbon dioxide, with a low level of hydrogen sulphide (H2S
                                        partial pressure < 0.05 psi). Sour corrosion takes place where the hydrogen
                                            sulphide’s partial pressure is above 0.05 psi. H2S itself is not corrosive,
                                            but a water-containing gas environment causes reactions which lead to
                                            corrosion of the inner surface of the steel pipeline.</p>
                                            <p>Sweet corrosion results in internal pitting of the pipeline, whereas sour
                                                corrosion is much more severe, and can compromise the structural
                                                soundness of the pipeline, leading to fatigue failure.</p>
                                            <p>Additionally, because of the long runs in unprotected environments,
                                                pipelines are exposed to external corrosion attack. All of the above
                                                leads to pipeline leakage, with a potential for ecological damage or
                                                danger to life.</p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h3>Storage Tanks And Gas Cylinders – Use And Abuse</h3>
                                </div>
                                <div class="text">
                                    <img src="assets/images/other/9.jpg" alt="" class="img-flow">
                                    <p>Oil and gas storage devices are vulnerable to “sweet” and “sour” corrosion
                                        similarly to pipelines, as explained above. In addition, moisture penetration
                                        inside the device, and its accumulation in the container bottom aggravates
                                        corrosion, especially in the lower part of the container. This moisture may also
                                        evaporate in warm weather, and with a fall of external temperature,
                                        re-condensate all over the internal surface of the container.</p>
                                    <p>Moisture penetration occurs mainly in the absence of an effective non-return
                                        filler valve and/or – for gas cylinders – when they are left open in a totally
                                        depressurised state. Domestic gas cylinders are also prone to external
                                        corrosion, caused by knocks during transport and when they are placed on damp
                                        surfaces while in service.</p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h3>The Hidden Cost Of Corrosion</h3>
                                </div>
                                <div class="text">
                                    <p>Corrosion damage is gradual, and it may take years until significant
                                        deterioration occurs; eventually resulting in leaks, equipment breakdowns, and
                                        structural failure. All of this has a sometimes-critical impact on: productivity
                                        (performance degradation and equipment failure), the environment (pollution
                                        through spillage), and above all safety (structural failure, fire, explosion and
                                        worse).</p>
                                    <p>Often the day-to-day economics of running an oil/gas installation discourages
                                        taking the long-term view with regard to corrosion maintenance – oil price
                                        instability causing profit margin fluctuations. In particular, with regard to
                                        extraction platforms, as reserves diminish at a particular well and future
                                        closure is anticipated, investment in expensive maintenance is discouraged –
                                        exactly at the stage when corrosion-protection maintenance becomes critical.</p>
                                    <p>The cost of corrosion protection is a significant item in the maintenance bill of
                                        contemporary oil and gas installations and equipment:</p>
                                    <ul>
                                        <li>Sea-borne extraction platforms, their risers (parts of which may be hundreds
                                            or even thousands, of metres underwater), the well and its drilling
                                            equipment (inaccessible under the seabed or deep underground) – both the
                                            severity of the working environment and the inaccessibility of equipment
                                            make inspection and corrosion maintenance very costly.</li>
                                        <li>Refinery plant is very vulnerable to internal corrosion. Due to the
                                            complexity of the equipment and the inability to directly reach internal
                                            piping, corrosion monitoring and mitigation methods require a substantial
                                            outlay.</li>
                                        <li>Pipelines and storage tanks, which are deployed over a wide geographic area
                                            and – in the case of domestic gas containers – may suffer abuse by the
                                            end-user, require distribution companies to spend time and resources in
                                            inspection and repair.</li>
                                    </ul>
                                    <p>According to a study by the U.S. Federal Highway Administration (FHWA) in 2002
                                        (Corrosion Costs and Preventive Strategies in the United States, PUBLICATION NO.
                                        FHWA-RD-01-156 – NACE International), in the USA, the cost of corrosion for gas
                                        distribution was estimated to be $5 billion, and for oil & gas exploration &
                                        production about $1.4 billion, with $0.6 billion attributed to surface piping
                                        and facility costs, $0.5 billion to downhole tubing, and $0.3 billion to capital
                                        expenditures related to corrosion.</p>
