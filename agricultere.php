                                    <h4>Machinery & Equipment</h4>
                                </div>
                                <div class="text">
                                    <p>Agricultural activity is seasonal, different machines being used in different
                                        seasons. Machines are stored away, sometimes for months on end, when seasonal
                                        activity (or inactivity) does not require their use – especially during cold
                                        humid winters. If they are not thoroughly cleaned before storage, the residue of
                                        the substances they handle will surely cause corrosion over the months.</p>
                                    <p>Machines that handle chemicals are the most vulnerable to corrosion, in
                                        particular those handling slurries and artificial fertilisers, which are highly
                                        corrosive, as already mentioned.</p>
                                    <p>Poorly drained areas and inner machine structures, where chemicals, dirt and
                                        moisture can accumulate are particularly vulnerable to corrosion. High humidity
                                        (leading to condensation) and poor ventilation of the hangars where machinery is
                                        stored off-season accelerates any corrosion process, sometimes significantly.
                                    </p>
                                    <p> <b>Which parts of the machinery are the most vulnerable to corrosion?</b> The
                                        inner surfaces of metal containers and piping which handle corrosive substances,
                                        overlapped joints, spot-welds, or any other crevices, as well as moving parts,
                                        e.g. cutters, feed mechanisms, bed chains, hinges.</p>
                                </div>
                                <div class="group-title">
