                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h4>Agricultural Chemical & Biological Products</h4>
                                </div>
                                <div class="text">
                                    <p><b>Fertilisers</b>– Some fertilisers have very corrosive decomposition
                                        by-products, such as ammonia, hydrogen sulphide or chlorides (including
                                        potassium or ammonium chloride).</p>
                                    <p><b>Silage</b>– is high-moisture fermented forage, used as food for ruminants. It
                                        undergoes fermentation over approx. 2 weeks from the time it is stored in the
                                        silo. In the process it releases a nitric-bearing corrosive liquid./p>
                                    <p><b>Herbicides and pesticides</b>– often contain copper-bearing fluids. For
                                        example, the “Bordeaux Mixture” is made from copper sulphate, water and lime,
                                        which attacks aluminium and zinc, thereby eroding the protective galvanic layer
                                        from equipment and structures made of galvanised steel.</p>
                                    <p><b>Slurries and manures</b>– Slurry is a mixture of animal dung and urine, and
                                        manure is slurry composted with straw, wood shaving and other waste. They have
                                        corrosive constituents, such as uric acid, ammonia, ammonium salts, and
                                        naturally excreted chloride.</p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h4>Structures</h4>
                                </div>
                                <div class="text">
                                    <img src="assets/images/other/11.jpg" alt="" class="img-flow">
                                    <p>Agriculture makes prolific use of steel structures and sheet-metal walls and
                                        partitions. As mentioned above, many agricultural chemical and biological
                                        substances are corrosive. Also, the environment takes its toll, especially in
                                        locations close to the sea or with high ambient humidity. Condensation is always
                                        a problem in humid climates having marked temperature differences between day
                                        and night.</p>
                                    <p>Much of the steel used is galvanised. However, corrosive substances, whether salt
                                        or acidic by-products erode the protective galvanic coating, exposing the
                                        underlying steel to corrosion. For example, in rural inland areas, a galvanised
                                        sheet-steel wall may last over 30 years without preventive maintenance, whereas
                                        in a humid or polluted environment, it would last 5 to 10 years.</p>
                                    <p>Additionally, certain soils (sulphate and chloride bearing peats and marshes)
                                        attack structure foundations.</p>
                                    <p> <b>Silos </b> are damaged by organic acids – A very acidic and corrosive
                                        environment prevails in silos containing crop maize silage, which rapidly
                                        ferments, producing acids with a typical concentration in solution of 2% lactic
                                        acid, 0.5% acetic acid, and a pH as low as 3.6.</p>
                                    <p> <b>Dairy farming infrastructure</b> – Milking equipment is generally made of
                                        stainless steel, which has excellent corrosion withstand. However, supporting
                                        structures, made of mild steel, are prone to corrosion by lactic acid- bearing
                                        milk wastes and chlorine-bearing cleaning/sterilisation agents.</p>
                                </div>
                                <div class="group-title">
