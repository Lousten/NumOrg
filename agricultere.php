                                    <h3>What Are The Consequences Of Corrosion In Agriculture?</h3>
                                </div>
                                <div class="text">
                                    <p>Corrosion reduces the metal thickness of structural elements, which leads to a
                                        loss of mechanical strength and the risk of structural failure – with the
                                        attendant risk of causing injury to people, animals and/or spillages.</p>
                                    <p>Leaks caused by corrosion can lead to fluids stored in vessels or flowing in
                                        pipes to be contaminated by foreign substances.</p>
                                    <p>Leaks also cause spillage, and consequent damage to surrounding equipment, to
                                        animals, and/or to the environment.</p>
                                    <p>Pumps and valves may suffer damage from solid corrosion products carried by the
                                        fluid which they handle.</p>
                                </div>
