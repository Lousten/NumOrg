                                    <p>Apart from the bodyshell, suspension components are exposed to mud, grit, slush,
                                        snow (and where used, anti-ice salt) sprayed from the road below.</p>
                                    <p>Exhaust systems corrode because of the water vapour exhausted by the engine in
                                        cold humid weather. If the exhaust system does not reach high temperature during
                                        operation, this water vapour condenses on its inner surfaces, causing corrosion.
                                    </p>

