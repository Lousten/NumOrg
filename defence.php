                                    <h3>Corrosion In The Defence Industry</h3>
                                </div>
                                <div class="text">
                                    <p><b>We Must Protect Our Military Assets From Corrosion. In Every Country, The
                                            Authority Responsible For Defence Acquires, Operates, And Maintains A Large
                                            And Diverse Array Of Physical Assets.</b></p>

                                    <p>Why must we protect our military assets from corrosion?</p>
                                    <p>In every country, the authority responsible for defence acquires, operates, and
                                        maintains a large and diverse array of physical assets:</p>
                                    <ul>
                                        <li>Weapon systems, mostly made of steel</li>
                                        <li>Ground, air and sea platforms and vehicles, e.g. tanks, personnel carriers,
                                            patrol ships, gunships – all mostly made of steel; as well as airplanes,
                                            helicopters, having critical parts made of corrodible metals.</li>
                                        <li>Infrastructure, e.g. buildings, port and airport installations, much of it
                                            built of steel-reinforced concrete.</li>
                                    </ul>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h3>Consequences Of Corrosion</h3>
                                </div>
                                <div class="text">
                                    <p>Since armed forces must train and fight in all environments, including in very
                                        corrosive environments, military assets are particularly vulnerable to
                                        degradation caused by corrosion. This has important implications in the
                                        following areas:</p>
                                    <ul>
                                        <li><b>Safety </b>– A number of recent weapons-related incidents have been
                                            attributed to corrosion – for example, corroded electrical contacts on US
                                            Air Force F-16s caused spurious fuel valve closures (resulting in in
                                            aircraft-loss), and corrosion-related landing gear cracking in F/A-18 s
                                            resulted in failures.</li>
                                        <li><b>Battle-readiness</b>– Corrosion routinely causes weapons systems to be
                                            out of commission. For example, approx. 50% of the KC-135 aircraft’s
                                            maintenance is attributable to corrosion problems.</li>
                                        <li><b>Financial loss</b>– It is estimated that corrosion costs the US
                                            Department of approx. $20 billion annually.</li>
                                    </ul>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h3>How Corrosion Affects Different Assets</h3>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h4>Aviation</h4>
                                </div>
                                <div class="text">
                                    <p>Approx 2% of US Army and Navy aircraft mishaps are attributable to corrosion
                                        between 1983 and 2013 (LMI – The Effect of Corrosion on Army and Department of
                                        the Navy – AKN31T4/May 2015).</p>

                                    <p>Mishaps primary attributable to corrosion were principally caused by: material
                                        failure in the engine’s first-stage turbine, the air seal/second stage vane,
                                        positioning lugs, bad electrical contacts.</p>

                                    <p>Microclimatic effects both inside and outside the fuselage, caused by large
                                        external temperature variations in high-humidity conditions accelerate
                                        corrosion.</p>

                                    <p>Apart from mishaps, corrosion also causes loss of combat-readiness.</p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h4>Naval</h4>
                                </div>
                                <div class="text">
                                    <img src="assets/images/other/5.jpg" alt="" class="img-flow">
                                    <p>Ships are primarily exposed to atmospheric corrosion, caused by a combination of
                                        high moisture and salt-laden sea spray, fretting corrosion, caused by the
                                        repeated relative surface motion between loaded metal surfaces, typically
                                        induced by vibration and structural flexing, galvanic corrosion caused by two
                                        metals forming a parasitic galvanic cell with sea water acting as an
                                        electrolyte, stray current corrosion caused by leakage earth currents flowing
                                        out of the ship’s hull to the pier-side power supply earth (when the ship is in
                                        dock), crevice corrosion, where water and sediment accumulate (due leaks and/ or
                                        inadequate drainage) , and microbial corrosion is caused by certain types of
                                        marine bacteria, which stick to the hull or accumulate in crevices.</p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h4>Ground Vehicles</h4>
                                </div>
                                <div class="text">
                                    <p>There is a great diversity of ground vehicles with a great variety of operational
                                        profiles and operational environments. Corrosion-related damage predominantly
                                        affects the vehicle’s structure, and is caused by humid conditions, water leaks
                                        and/or defective water drainage from inner structures, and physical damage to
                                        the protective paint layer, exposing the bare metal.</p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h4>Weapons Systems</h4>
                                </div>
                                <div class="text">
                                    <p>Physical deterioration of weapon systems due to corrosion leads to reduced
                                        functionality, loss of structural integrity, reduced safety margins, and danger
                                        to the operator and his surroundings. Corrosion also leads to increased
                                        operating and support cost.</p>
                                    <p>However, due to the diversity of weapons systems, the overall extent of
                                        corrosion-related weapons mishaps is difficult to quantify.</p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h4>Infrastructure</h4>
                                </div>
                                <div class="text">
                                    <p>Today, steel-reinforced concrete (i.e. concrete with buried steel reinforcement
                                        bars) is widely used in most types of infrastructure – buildings, harbour
                                        installations, defence installations (e.g. bunkers), and more.</p>
                                    <p>Despite its appearance, concrete is typically wet, and therefore the buried steel
                                        bars are in a wet environment. However, concrete is intrinsically very alkaline
                                        (high pH), which prevents oxidation of its steel bars. Anything which
                                        neutralises the concrete’s alkalinity, allowing oxygen to penetrate and
                                        eventually to reach the steel bars, is a source of corrosion: Chlorine ions
                                        (mainly from salt-laden seaside installations), water-borne atmospheric carbon
                                        dioxide (which forms carbonic acid), sulphur and nitrous oxide, which are
                                        essentially acidic.</p>
                                    <p>Mechanical stresses and the effects of the environment (both temperature cycling
                                        and humidity), accelerate corrosion, by causing or increasing cracks, through
                                        which oxygen reaches the steel bars more easily.</p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h3>How Much Does It Cost?</h3>
                                </div>
                                <div class="text">
                                    <img src="assets/images/other/6.jpg" alt="" class="img-flow">
                                    <p>In 2013, the US Department of Defence spent approx. $20.8 billion annually to
                                        prevent and mitigate the corrosion of its assets – principally military
                                        equipment, weapons, facilities and other infrastructure. While the majority, by
                                        far, of these costs are attributable to corrosion issues on military equipment
                                        and weapons, the cost of corrosion at facilities and other infrastructure was
                                        estimated at approx. $1.9 billion annually.</p>
                                    <p>A number of studies have been undertaken to assess the cost of corrosion for
                                        different types of assets:</p>
                                    <p><b>Aviation </b>– Corrosion accounts for approx. a quarter of all maintenance
                                        costs and about 15% of all system non-available hours (NAH) for US Air Force
                                        aircraft and missiles (Annual Corrosion Prevention and Control Report for Fiscal
                                        Year 2014; US AIR FORCE; DEC 22, 2014).</p>
                                    <p><b>Naval </b> – The overall cost of corrosion for US Navy ships was estimated at
                                        $3.15 billion for the 2010 financial year (Estimate of the Annual Cost of
                                        Corrosion for Navy Ships: FY2008–10 Update; LCI, DAC21T1/SEPTEMBER 2012)</p>
                                    <p><b>Ground vehicles</b>– Corrosion cost for US Army ground vehicles is 12.6 % of
                                        the total maintenance costs for these vehicles. (CorrDefense Volume 9, Number 1
                                        Spring 2013 Inside DOD: DOD estimates the Effect of Corrosion on the Cost and
                                        Availability of Army Ground Vehicles).

                                    </p>
                                    <p><b>Weapon systems</b>– are very expensive in comparison with other military
                                        assets, therefore any corrosion damage is much more expensive to repair.
                                        However, it is estimated by the US Department of Defence that proper corrosion
                                        prevention measures can save up to 30% of weapon systems maintenance costs
                                        (Defense Science Board Report on Corrosion Control; OFFICE OF THE UNDER
                                        SECRETARY OF DEFENSE FOR ACQUISITION, TECHNOLOGY, AND LOGISTICS; Oct 2004).</p>
                                    <p><b>Infrastructure </b>– Typically, corrosion is found on docks, piers, mooring
                                        structures, bulkheads, retaining walls, and navigational aids (such as
                                        lighthouses). The cost of corrosion at US Department of Defence facilities and
                                        other infrastructure was estimated at approx. $1.9 billion annually (Defense
                                        Infrastructure: DOD’s 2013 Facilities Corrosion Study Addressed Reporting
                                        Elements; US GOVERNMENT ACCOUNTABILITY OFFICE; Mar 27, 2013).</p>
