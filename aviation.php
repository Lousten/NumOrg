                                    <h3>What Factors Accelerate Corrosion</h3>
                                </div>

                                <div class="text">
                                    <p><b>The environment </b>– The environment under which an aircraft operates greatly
                                        affects its corrosion characteristics. A marine environment with its
                                        sea-water-laden air is the most detrimental, a desert environment the most
                                        benign. Temperature also have a significant effect on the rate of corrosion – a
                                        hot humid climate being the most detrimental.</p>
                                    <p><b>Extreme temperature and pressure cycling</b>– By the nature of its service an
                                        airplane undergoes large cyclical temperature and pressure changes, as it climbs
                                        into the high atmosphere or lands at its destination. Temperatures in the high
                                        atmosphere, even in tropical latitudes, may reach -40°C, and pressures can drop
                                        as low as 0.2 atmospheres.</p>
                                    <p>This causes:</p>
                                    <ul>
                                        <li><b>Ambient moisture to condense</b>on the aircraft – on its outer skin, in
                                            its inner structural surfaces, and on important components, such as
                                            electrics and landing gear; moisture then becoming trapped inside these
                                            complex structures;</li>
                                        <li><b>Seals to leak</b>over time, resulting in the penetration of moisture
                                            (driven by pressure differentials) even into the nominally sealed areas of
                                            the structure; this happens gradually, over a long timescale.</li>
                                    </ul>
                                    <p><b>Foreign matter</b>– such as battery acid spillage, caustic cleaning solutions,
                                        engine exhaust residues (due to its sulphur content), atmospheric dust and soil.
                                    </p>
                                    <p><b>Leaking lavatories and galleys</b>– sometimes not easily detected, cause the
                                        accumulation of humidity in inner structures.</p>
                                    <p>And as the aircraft ages, the cumulation of these factors make the problem of
                                        corrosion more severe.</p>
                                </div>
