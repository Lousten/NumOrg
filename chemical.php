                                    <h3>How Does Corrosion Affect Particular Industries?</h3>
                                </div>
                                <div class="text">
                                    <img src="assets/images/other/7.jpg" alt="" class="img-flow">
                                    <p><b>Food Industry </b></p>
                                    <p>Food corrosivity can be divided into three categories:</p>

                                    <p>Non-corrosive – for example, milk, fish, meat, oil, fat and cereals
                                        Mildly corrosive – foods having a pH of 6 to 7, and having less than 1% salt,
                                        for example, wine, beer, carbonated sweet drinks, fruit syrups, soups and canned
                                        meat
                                        Highly corrosive – foods having a pH of 3 to 5, for example, citric fruit
                                        juices, acidic canned fruit, jam, brine-pickled vegetable and fish (with 1–3 %
                                        salt), gravies and salad dressings.
                                        Corrosion is also caused by the use of cleaning and disinfecting agents,
                                        required to maintain food safety standards: Three types of cleaning and
                                        disinfection agents are used:</p>

                                    <p>Alkaline – g. caustic soda, alkali phosphates, sodium carbonate and bicarbonate
                                        Acidic – e.g. phosphoric, citric and sulphamic acids
                                        Oxidizers – e.g. chlorine, nitric acid, ozone, hypochlorite, hydrogen peroxide.
                                        Acidic and oxidising substances which flow in the processing plant pipework, and
                                        in processing and storage vats cause internal corrosion.</p>

                                    <p>Therefore, the processing and storage vessels for certain types of food are
                                        vulnerable to internal corrosion. Products used to clean the processing plants –
                                        hygiene is of prime importance in the food industry – may also corrode the plant
                                        from the inside.</p>
                                    <p><b>Pharmaceutical Industry</b></p>
                                    <p>In the pharmaceutical industry, glass and stainless steel are the principal
                                        materials used for the construction of processing equipment. The development of
                                        increasingly sophisticated biochemical and pharmaceutical products and processes
                                        is being limited by what can be synthesised in stainless-steel containers (where
                                        glass is unsuitable).</p>

                                    <p>Despite stainless steel being stronger and more corrosion-resistant than mild
                                        steel, stainless steel corrodes over time as its minor ingredients are lost,
                                        giving rise to electrochemical potentials which cause the metal to oxidise –
                                        thus compromising the quality of the products being manufactured.</p>

                                    <p>Glass is brittle and does not endure thermal cycling well. In many process
                                        plants, the glass cracking exposes the underlying iron or steel substrate to the
                                        process fluids.</p>
                                    <p><b>Oil Refining Industry</b></p>
                                    <p>The inner surfaces of the refining apparatus are attacked by the acidic content
                                        of the oil being refined, principally the oil’s sulphur content (sour
                                        corrosion), thiol content (mercaptans) and its naphthenic acids. Corrosion is
                                        aggravated by the high pressures, temperatures and flows used in the process.
                                    </p>
                                    <p>Corrosion predominantly occurs in the crude distillation unit (CDU) and in the
                                        high vacuum unit (HVU) where the crude oil is distilled in two steps, at
                                        atmospheric pressure and under vacuum, into its major fractions.</p>
