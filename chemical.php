                                    <h3>What Common Materials Used In The Chemical Industry Drive Corrosion?</h3>
                                </div>
                                <div class="text">
                                    <p>Chemical industry corrosion is mainly caused by the liquids and gases used in
                                        chemical processes. They corrode the processing apparatus and the storage
                                        containers from the inside by attacking their inner metallic surfaces. Two types
                                        of substances dominate the chemical industry: Concentrated sulphuric acids and
                                        organic acids, each bringing particular corrosion problems.</p>
                                    <p><b>Concentrated sulphuric acid</b> is a key substance in the chemical industry.
                                        It is used for making fertilisers and cleaning agents, and as an electrolyte in
                                        lead-acid batteries. It is also used in oil refining and mineral processing. It
                                        is aggressively corrosive and attacks the inner surfaces of steel tubing and
                                        storage containers.</p>
                                    <p>Carbon steel is commonly used in processes involving this acid, because of its
                                        economic price and despite its high vulnerability to corrosion. Other steels,
                                        such as austentic steel are more resistant to corrosion, but are significantly
                                        more expensive. Therefore, even when corrosion-protection measures are
                                        implemented, the processing apparatus corrodes away over time.</p>
                                    <p><b>Organic acids</b>, of which there is an almost unlimited variety of compounds,
                                        constitute another important group of substances in the chemical industry. These
                                        acids are in general only mildly acidic in comparison to inorganic acids like
                                        sulphuric acid (discussed above). However, they cause CO2 corrosion in steel
                                        (corrosion caused by the dissolution of iron in carbonic acid solutions
                                        containing chloride). </p>
                                    <p>When steel is exposed to a carbonic acid solution, it initially corrodes rapidly.
                                        As carbonate scale forms, the corrosion rate decreases, eventually reaching a
                                        steady state. If carbonate scale does not form, the corrosion rate will remain
                                        high – this depends on the aqueous-phase salinity and temperature. Typically,
                                        when the aqueous / brine phase is similar to seawater, this maximum occurs at
                                        temperatures between 15°C and 65°C.</p>
                                </div>
