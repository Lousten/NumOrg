                                    <h3>Corrosion In Reinforced Concrete Infrastructure</h3>
                                </div>
                                <div class="text">
                                    <p><b>Today, Steel-reinforced Concrete Is Widely Used In Most Types Of
                                            Infrastructure. Buildings, Roadway & Railway Terracing & Support, Bridges,
                                            Harbour Installations, Dams.</b></p>
                                    <img src="assets/images/other/3.jpg" class="img-flow" alt="">
                                    <p>Since concrete can only take compressive stress, and not tensional stress, it
                                        must always be reinforced by internal steel bars, which are seemingly protected
                                        from the ravages of the external environment. They are certainly out of sight,
                                        buried in the body of the concrete…Out of sight, out of mind? Will they
                                        eventually rust? The short answer is yes.</p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h3>The Mechanisms Of Corrosion</h3>
                                </div>
                                <div class="text">

                                    <p>Despite its appearance, concrete is typically wet, and therefore the steel bars
                                        are in a wet environment. However concrete is intrinsically very alkaline (high
                                        pH), which prevents oxidation of the steel bars buried inside.</p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h4>A.Contaminants</h4>
                                </div>
                                <div class="text">
                                    <p>Anything which neutralises the alkalinity of the concrete, allows oxygen to
                                        penetrate
                                        the concrete’s complex cement matrix, and eventually reach the steel bars.
                                        Common
                                        contaminants are:</p>
                                    <ul>
                                        <li><b>Chloride ions</b>, found in abundance in sea-salt spray and de-icing
                                            compounds (typically salt) applied to roadways in snowy weather</li>
                                        <li><b>Water-borne atmospheric carbon dioxide</b> reacting with the lime from
                                            the
                                            cement to form carbonic acid
                                        </li>
                                        <li><b>Sulphur and nitrous oxide</b>, found in urban polluted atmospheres are
                                            essentially acidic.</li>
                                    </ul>
                                    <p>All of the above lead to a loss of alkalinity and thus facilitates the
                                        penetration of
                                        oxygen through the cement matrix, right down to the steel bars.</p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h4>B.Mechanical Stresses</h4>
                                </div>
                                <div class="text">
                                    <p>Structures are always subject to mechanical stresses: vibration, load changes,
                                        environmentally induced stresses (discussed further on). Mechanical stresses
                                        cause slight deformation of the concrete, which may lead to micro-cracking. When
                                        the concrete cracks, oxidisers can reach the internal steel bars more easily.
                                    </p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h4>C.The Effect Of The Environment</h4>
                                </div>
                                <div class="text">
                                    <ul>
                                        <li><b>Temperature </b>– In general higher temperatures accelerate any ongoing
                                            corrosion process; at sub-zero temperatures the corrosion process is
                                            considerably slowed-down.</li>
                                        <li><b>Humidity </b> – High relative humidity accelerates the corrosion process.
                                            The effect of humidity is determined by the Time Of Wetness (TOW), i.e. a
                                            measure of the time for which atmospheric conditions favour the formation of
                                            a moisture layer on the structure’s surface. Humidity neutralises alkalinity
                                            and thereby facilitates oxygen penetration.</li>
                                        <li><b>Marine environment</b>– High humidity conditions, together with sea spray
                                            salinity, throws an abundance of chloride ions at the concrete, which
                                            neutralises alkalinity and thereby facilitates oxygen penetration.</li>
                                        <li><b>Soil exposure</b>– When water freezes, its volume expands by a little
                                            less than 10 percent. Any humidity present in small cracks will therefore
                                            force these cracks open upon freezing. Repeated freeze-thaw cycles, caused
                                            by cyclical ambient temperature changes will widen the cracks, thus
                                            facilitating oxygen penetration to the buried steel bars.</li>
                                        <li><b>Environmental differences</b>– Differences in oxygen concentration,
                                            acidity, moisture or salinity between different parts of the structure
                                            facilitate the penetration of oxygen into the cement matrix.</li>

                                    </ul>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h4>D.Stagnant Water And Drainage</h4>
                                </div>
                                <div class="text">
                                    <p>In many concrete structures, water (typically rain water) runoff is deficient,
                                        either because of the physical shape of the concrete elements, or because
                                        drainage channels have become obstructed. In either case, stagnant water pools
                                        collect in certain locations – salt water in marine environments – and the
                                        concrete’s alkalinity at these locations is diminished, leading to oxygen
                                        penetration to the buried steel bars.</p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h4>E.Feedback Loop</h4>
                                </div>
                                <div class="text">
                                    <p>When an internal steel bar starts to corrode due to any combination of the above,
                                        its volume increases. This generates pressure inside the concrete element,
                                        widens any existing cracks and generates new cracks from the inside to the
                                        outside. This in turn increases the exposure of the steel to oxygenation and
                                        accelerates the corrosion process. Thus, further corrosion is also accompanied
                                        by further cracking. Therefore, over time, the corrosion process is accelerated.
                                    </p>
                                </div>
                                <div class="group-title">
                                    <div class="line" style="background-image: url(assets/images/icons/line-4.png);">
                                    </div>
                                    <h3>Consequences Of Corrosion</h3>
                                </div>
                                <div class="text">
                                    <p>Steel corrosion inside the concrete element has two main consequences:</p>
                                    <ul>
                                        <li>Corrosion-induced volume changes of the steel bars inside the concrete cause
                                            the concrete to crack and eventually break-up.</li>
                                        <li>The steel bars themselves become weaker and more brittle by corrosion,
                                            leading to further loss of strength of the concrete.</li>
                                    </ul>
                                    <p>Both of the above lead to the likelihood of structural failure, especially during
                                        severe external conditions, or heavy loading – many recent road-bridge failures
                                        have been attributed to this.</p>
                                </div>
